# Dictionary storing information about a person
person = {
    'first_name': 'Emily',
    'last_name': 'Johnson',
    'age': 29,
    'city': 'Seattle'
}

# Printing each piece of information
print(f"First name: {person['first_name']}")
print(f"Last name: {person['last_name']}")
print(f"Age: {person['age']}")
print(f"City: {person['city']}")
